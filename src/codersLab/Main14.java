package codersLab;

public class Main14 {

	public static void main(String[] args) {
		for(int n=0;n<=6;n++) {
			if (n % 2 == 0) {
				System.out.println( n + " - even ");
			} else {
				System.out.println( n + " - odd ");
			}
		}
		int m = 0;
		while (m <= 6){
			if (m % 2 == 0) {
				System.out.println( m + " - even ");
			} else {
				System.out.println( m + " - odd");
			}
			m = m+1;
	}
	}

}
